use std::cmp::max;

use ratatui::{
    text::{Span, Spans, Text},
    widgets::Paragraph,
};

pub struct ParagraphArea<'a> {
    pub paragraph: Paragraph<'a>,
    pub height: u16,
    pub width: u16,
}

impl ParagraphArea<'static> {
    pub fn new(value: &'static str) -> ParagraphArea {
        let mut width: u16 = 0;
        let lines: Vec<Spans> = value
            .lines()
            .map(|str| {
                width = max(width, str.len() as u16);
                Spans::from(Span::raw(str))
            })
            .collect();

        Self {
            height: lines.len() as u16,
            paragraph: Paragraph::new(Text::from(lines)),
            width,
        }
    }
}

